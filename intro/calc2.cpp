#include<iostream> 

using namespace std; 


int main () {
    long long  m , n ; 
    cin >> m >> n ; 
    cout << m << " + " << n  << " = " << m + n  << endl ; 
    cout <<endl;  
    cout << m << " - " << n  << " = " << m - n  << endl ;
    cout <<endl;  
 
    cout << m << " * " << n  << " = " << m * n  << endl ; 
    cout <<endl;  

    cout << m << " / " << n  << " = " << m / n  << endl ; 
    cout <<endl;  

    cout << m << " % " << n  << " = " << m % n  << endl ; 


    return 0 ; 
 
}